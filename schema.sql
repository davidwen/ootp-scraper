CREATE TABLE IF NOT EXISTS hall_of_fame (
    player_id INTEGER,
    year INTEGER,
    FOREIGN KEY (player_id) REFERENCES players(id));

CREATE TABLE IF NOT EXISTS hall_of_fame_eligible (
    player_id INTEGER,
    FOREIGN KEY (player_id) REFERENCES players(id));

